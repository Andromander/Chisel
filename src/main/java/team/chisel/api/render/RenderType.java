package team.chisel.api.render;

/**
 * Enum for different types of chisel block rendering
 */
public enum RenderType {
    NORMAL("normal"),
    CTM("ctm"),
    CTMH("ctmh"),
    CTMV("ctmv"),
    V9("v9"),
    V4("v4"),
    R16("r16"),
    R9("r9"),
    R4("r4");

    private String name;

    RenderType(String name){
        this.name = name;
    }

    public String getName(){
        return this.name;
    }

    public boolean isCTM(){
        return this == CTM || this == CTMH || this == CTMV;
    }
}
